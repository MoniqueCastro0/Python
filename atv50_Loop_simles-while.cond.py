soma = 0

while soma < 100: #Enquanto for verdadeiro vai execultar
    numero = int(input("Digite um número: ")) #cada vez que execulta vai pedir um número
    soma += numero #soma inicialmente esta como 0 mas cada vez que rodar vai somar mais o numero
    #2° vez que rodar vai ser o soma mais o numero digitado
    print(soma)

print("Fim")
     